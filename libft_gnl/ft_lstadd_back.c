#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*list;

	if (!new)
		return ;
	if (!*lst)
		*lst = new;
	else
	{
		list = *lst;
		list = ft_lstlast(list);
		list->next = new;
	}
}
